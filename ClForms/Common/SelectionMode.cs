namespace ClForms.Common
{
    /// <summary>
    /// Defines the selection behavior for a list
    /// </summary>
    public enum SelectionMode
    {
        /// <summary>
        /// The user cannot select any item
        /// </summary>
        None,

        /// <summary>
        /// The user can select only one item at a time
        /// </summary>
        One,
    }
}
