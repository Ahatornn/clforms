namespace ClForms.Common
{
    /// <summary>
    /// Specifies whether the text in the object is left-aligned, right-aligned, centered
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// Default. Text is aligned to the left
        /// </summary>
        Left,

        /// <summary>
        /// Text is aligned to the right
        /// </summary>
        Right,

        /// <summary>
        /// Text is centered
        /// </summary>
        Center
    }
}
